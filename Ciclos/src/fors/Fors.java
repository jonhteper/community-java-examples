package fors;

public class Fors {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) { // for 5 iniciando en 1
            System.out.print(i+",");
        }
        System.out.println("");

        for (int i = 2; i <= 10; i++) { // for 10 iniciando en 2
            System.out.print(i+",");
        }

        System.out.println("");
        for (int i = 3; i <= 25; i++) { // for 25 iniciando en 3
            System.out.print(i+",");
        }
    }
}
